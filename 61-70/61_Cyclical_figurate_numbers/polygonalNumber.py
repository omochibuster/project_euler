def triangleNumber(number = 1):
	return number * (number + 1) // 2

def squareNumber(number = 1):
	return number ** 2

def pentagonalNumber(number = 1):
	return number * (3 * number - 1) // 2

def hexagonalNumber(number = 1):
	return number * (2 * number - 1)

def heptagonalNumber(number = 1):
	return number * (5 * number - 3) // 2

def octagonalNumber(number = 1):
	return number * (3 * number - 2)